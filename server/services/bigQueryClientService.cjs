const { BigQuery } = require('@google-cloud/bigquery');
const path = require('path');
const fs = require('fs');

const KEY_FILE_PATH = path.join(__dirname, '../config/work-jindan-194620a46d59.json');
const DATASET_ID = 'daily_log_system';

let _client = null;

function getBigQueryClient() {
  if (_client) return _client;

  if (!fs.existsSync(KEY_FILE_PATH)) {
    console.warn('[BigQuery] ???뚯씪 ?놁쓬:', KEY_FILE_PATH);
    return null;
  }

  try {
    _client = new BigQuery({ keyFilename: KEY_FILE_PATH });
    return _client;
  } catch (err) {
    console.error('[BigQuery] ?대씪?댁뼵??珥덇린???ㅽ뙣:', err.message);
    return null;
  }
}

